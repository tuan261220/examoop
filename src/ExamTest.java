    private void setTimeConvert(long time) {
        int h = (int) (time / 3600);
        int m = (int) ((time % 3600) / 60);
        int s = (int) ((time % 3600) % 60);
        if (h > 0) {

            if (m > 0) {
                lbTime.setText(h + " : " + m + " : " + s);
            }

        } else {
            if (m > 0) {
                lbTime.setText("00 :" + m + " : " + s);

            } else {
                lbTime.setText("00 : 00: " + s);
            }
        }
    }

    private void setTimeProgress() {

        timebar.setMaximum((int) time);
        long temp = time;
        timebar.setMinimum(0);
        timmer = new Timer(1000, new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {

                if (timebar.getValue() < temp) {

                    int h = (int) (time / 3600);
                    int m = (int) ((time % 3600) / 60);
                    int s = (int) ((time % 3600) % 60);
                    if (h > 0) {

                        if (m > 0) {
                            timebar.setString(h + " : " + m + " : " + s);
                        }

                    } else {
                        if (m > 0) {
                            timebar.setString(m + " : " + s);

                        } else {
                            timebar.setString("00 : " + s);
                        }
                    }
                    time--;
                    seconds++;
                    timebar.setValue(seconds);
                } else {
                    timmer.stop();

                    int show = JOptionPane.showConfirmDialog(null, "Hết thời gian làm bài", "Time up", 2);

                    if (show == 0) {
                        chamDiem();
                    }

                }

            }
        });
    }


    private void chamDiem() {
        float temp = (tongDiem / dapAn.length);
        for (int i = 0; i < dapAn.length; i++) {
            System.out.println(dsChon[i]);

            if (dsChon[i] == dapAn[i]) {
                diem += temp;

            }
        }

        DecimalFormat df = new DecimalFormat("#.##");
        String dx = df.format(diem);
        diem = Float.valueOf(dx);

        JOptionPane.showMessageDialog(null, "Bạn được :" + diem);

        diem = 0;

        comfirmDialog();
    }

